    static CookData()
    {
        // Envelope quantization tables (13 codebooks, max symbol 23)
        QuantCodes = new ushort[13][];
        QuantBits = new byte[13][];
        for (int i = 0; i < 13; i++)
        {
            int maxSym = 24;
            QuantCodes[i] = new ushort[maxSym];
            QuantBits[i] = new byte[maxSym];
            BuildCanonicalHuffman(EnvHuffCounts[i], EnvHuffSyms[i], QuantCodes[i], QuantBits[i]);
        }

        // VQ tables (7 codebooks)
        VqCodes = new ushort[7][];
        VqBits = new byte[7][];

        // VQ0-2 and VQ4-6 use 8-bit symbols
        int[] vq8Indices = { 0, 1, 2, 4, 5, 6 };
        int[] vq8DataIdx = { 0, 1, 2, 3, 4, 5 }; // index into VqHuffSyms8
        for (int k = 0; k < vq8Indices.Length; k++)
        {
            int cat = vq8Indices[k];
            var syms8 = VqHuffSyms8[vq8DataIdx[k]];
            int maxCodeword = ComputeMaxCodeword(cat);
            VqCodes[cat] = new ushort[maxCodeword];
            VqBits[cat] = new byte[maxCodeword];
            // Convert 8-bit syms to ushort for unified builder
            var syms16 = new ushort[syms8.Length];
            for (int j = 0; j < syms8.Length; j++)
            {
                syms16[j] = syms8[j];
            }
            BuildCanonicalHuffmanU16(VqHuffCounts[cat], syms16, VqCodes[cat], VqBits[cat]);
        }

        // VQ3 uses 16-bit symbols (520 entries)
        {
            int maxCodeword = ComputeMaxCodeword(3);
            VqCodes[3] = new ushort[maxCodeword];
            VqBits[3] = new byte[maxCodeword];
            BuildCanonicalHuffmanU16(VqHuffCounts[3], VqHuffSyms3_16, VqCodes[3], VqBits[3]);
        }

        // Coupling tables (5 codebooks for js_bits 2-6)
        CplCodes = new ushort[5][];
        CplBits = new byte[5][];
        for (int i = 0; i < 5; i++)
        {
            int maxSym = CplHuffSyms[i].Length;
            CplCodes[i] = new ushort[maxSym];
            CplBits[i] = new byte[maxSym];
            BuildCanonicalHuffman(CplHuffCounts[i], CplHuffSyms[i], CplCodes[i], CplBits[i]);
        }
    }

    /// <summary>Compute maximum codeword index for a VQ category (M^dim).</summary>
    private static int ComputeMaxCodeword(int cat)
    {
        int m = VqMult[cat]; // kmax: number of centroid levels
        int dim = VqGroupSize[cat];
        int result = 1;
        for (int i = 0; i < dim; i++)
        {
            result *= m;
        }
        return result;
    }

    /// <summary>
    /// Build encoding lookup tables from canonical Huffman (huffcounts + huffsyms).
    /// The canonical Huffman assigns codes in increasing order at each bit length.
    /// codes[symbol] and bits[symbol] are populated for encoding.
    /// </summary>
    private static void BuildCanonicalHuffman(byte[] huffcounts, byte[] huffsyms, ushort[] codes, byte[] bits)
    {
        int symIdx = 0;
        int code = 0;
        for (int bitLen = 1; bitLen <= 16; bitLen++)
        {
            for (int j = 0; j < huffcounts[bitLen - 1]; j++)
            {
                int symbol = huffsyms[symIdx++];
                if (symbol < codes.Length)
                {
                    codes[symbol] = (ushort)code;
                    bits[symbol] = (byte)bitLen;
                }
                code++;
            }
            code <<= 1;
        }
    }

